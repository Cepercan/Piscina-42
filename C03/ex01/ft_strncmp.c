int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int				k;
	unsigned int	i;

	k = 0;
	i = 0;
	while (i < n && (s2[i] != '\0' || s1[i] != '\0'))
	{
		if (s1[i] == s2[i])
			i++;
		else
		{
			k = s1[i] - s2[i];
			return (k);
		}
	}
	return (k);
}
