int	ft_iterative_factorial(int nb)
{
	int	n;

	n = nb;
	if (nb < 0)
		n = 0;
	if (nb == 0)
		n = 1;
	nb--;
	while (nb > 0)
	{
		n *= nb;
		nb--;
	}
	return (n);
}
